Director''s loan accounts are one of the most misunderstood aspects of running a limited company. Get it wrong, and you could face unexpected tax bills, benefit in kind charges, and HMRC scrutiny. This guide explains everything UK directors need to know about DLAs in 2025.

**What is a Director''s Loan Account?**

A director''s loan account (DLA) is a record of all transactions between a director and their company that are not salary, dividends, or expense reimbursements. It tracks money flowing in both directions:

**Money you put INTO the company:**
• Personal funds used to start the business
• Loans to help with cash flow
• Payments for company expenses from personal funds

**Money you take OUT of the company:**
• Cash withdrawals
• Personal expenses paid by the company
• Use of company assets

The DLA balance can be in credit (the company owes you money) or overdrawn (you owe the company money). The tax implications differ dramatically between these two states.

**Understanding Overdrawn and Credit Balances**

**Credit Balance (Company Owes You)**

When your DLA is in credit, the company owes you money. This can happen when:
• You''ve loaned the company money
• You''ve paid company expenses personally
• The company hasn''t fully reimbursed you

**Tax implications:** Generally none. You can withdraw this money at any time without tax consequences. The company may choose to pay you interest on the loan, which would be taxable income for you but an allowable expense for the company.

**Overdrawn Balance (You Owe the Company)**

When your DLA is overdrawn, you owe the company money. This creates potential tax issues:
• Section 455 tax for the company
• Possible benefit in kind for you
• Potential dividend treatment

**WARNING_BOX An overdrawn director''s loan account is one of the most common triggers for HMRC enquiries into small companies. Maintaining proper records and understanding the rules is essential.**

**Section 455 Tax Explained**

Section 455 of the Corporation Tax Act 2010 requires companies to pay a special tax charge when directors have overdrawn loan accounts that are not repaid within 9 months after the company''s accounting year end.

**How it works:**

**STEP_HEADER Step 1: Year End Assessment**

At your company''s year end, if your DLA is overdrawn, the company notes this on its Corporation Tax return.

**STEP_HEADER Step 2: The 9-Month Deadline**

You have 9 months after your accounting year end to clear the balance. If your year end is 31 March 2025, the deadline is 31 December 2025.

**STEP_HEADER Step 3: S455 Tax Payment**

If the loan isn''t repaid by the deadline, the company must pay S455 tax at **33.75%** of the outstanding loan amount. This is paid along with the Corporation Tax.

**STEP_HEADER Step 4: Claiming the Tax Back**

S455 tax is refundable once the loan is repaid, but there''s a significant delay. You can only reclaim it 9 months after the end of the accounting period in which the loan was repaid.

**Example:**
• Year end: 31 March 2025
• Overdrawn DLA at year end: £10,000
• S455 tax due (if not repaid by 31 Dec 2025): £3,375
• Loan repaid: 15 January 2026
• Tax refund available: 1 January 2027

**Benefit in Kind Implications**

If your overdrawn loan exceeds £10,000 at any point during the tax year and the company doesn''t charge you interest (or charges below the official rate), you''ll face benefit in kind (BIK) consequences.

**The Official Rate**

For 2025/26, the official rate of interest is **2.25%**. If you pay at least this rate, no BIK arises.

**BIK Calculation**

The benefit is the difference between interest at the official rate and any interest you actually pay.

**Example:**
• Average overdrawn balance during year: £50,000
• Official rate: 2.25%
• Interest at official rate: £1,125
• Interest you paid: £0
• Taxable benefit: £1,125

**Tax on the benefit:**
• For you: Income tax on £1,125 at your marginal rate
• For the company: Class 1A NIC at 13.8% = £155.25

The company must report this on a P11D form.

**The 9-Month Rule**

The 9-month rule is crucial for avoiding S455 tax:

• **Corporation Tax payment deadline:** 9 months and 1 day after year end
• **S455 applies if:** Loan is still outstanding at this point
• **Repayment window:** You have until the CT payment deadline to clear the loan

**Clearing an Overdrawn DLA**

You can clear an overdrawn DLA by:
1. **Repaying the money** - Transfer personal funds to the company
2. **Declaring a dividend** - Use dividends to offset the loan (dividend tax applies)
3. **Voting a salary/bonus** - Income tax and NIC apply
4. **Writing off the loan** - Treated as salary, full PAYE/NIC applies

**WARNING_BOX The "Bed and Breakfasting" rule: If you repay a loan and borrow again within 30 days, HMRC treats the arrangement as if the loan was never repaid. This prevents artificial repayment to avoid S455 tax.**

**Common Pitfalls to Avoid**

**1. Not Keeping Accurate Records**

Every transaction through the DLA should be documented with dates, amounts, and descriptions. Many directors don''t realise their account is overdrawn until it''s too late.

**2. Using the Company as a Personal Bank Account**

Treating company funds as your own leads to a constantly overdrawn DLA and ongoing tax complications.

**3. Ignoring the £10,000 BIK Threshold**

Even short periods where your loan exceeds £10,000 can trigger benefit in kind reporting requirements.

**4. Missing the 9-Month Deadline**

Diary the date 9 months after your year end. Repaying even one day late means paying S455 tax.

**5. Bed and Breakfasting**

Repaying loans temporarily to avoid tax, then borrowing again quickly, is caught by anti-avoidance rules.

**6. Mixing Personal and Business Expenses**

Always keep personal and business spending separate. Use a dedicated business card and account.

**Best Practices for Managing Your DLA**

**1. Review Monthly**

Check your DLA balance at least monthly, not just at year end.

**2. Set Up Proper Expense Claims**

Create a formal expense claim process rather than paying personally and "sorting it out later."

**3. Take Regular Dividends**

If you need income, declare and document proper dividends rather than withdrawing cash against your DLA.

**4. Consider Interest-Bearing Loans**

If you lend money to the company, consider charging interest. The company gets a tax deduction, and you declare the interest as income.

**5. Plan Ahead for Year End**

Well before your year end, review the DLA position and plan how to clear any overdrawn balance.

**6. Keep Company Funds Separate**

Resist the temptation to use company money for personal expenses. It creates complications and risks.

**7. Document Everything**

Every transaction should have supporting documentation. If HMRC enquires, good records are your best defence.

**Record-Keeping Requirements**

For each DLA transaction, record:
• Date of transaction
• Amount
• Description (what the money was for)
• Whether it''s a debit (you taking) or credit (you putting in)
• Running balance

