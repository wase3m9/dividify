**Common Pitfalls and Risks**

**1. Signing After the Dividend is Declared**

The waiver is ineffective. The shareholder is treated as having received the dividend and then gifting it.

**2. No Commercial Justification**

Without genuine business reasons, HMRC may successfully challenge under settlements legislation.

**3. Inconsistent Treatment**

Waiving dividends sometimes but not others, with no clear pattern or reason, raises red flags.

**4. Informal Arrangements**

Verbal agreements or casual emails are not sufficient. Use a properly executed deed.

**5. Ignoring the "Bounteous" Test**

If a shareholder waives so another can receive "bounty" (benefit beyond what they would normally get), it looks like a settlement.

**6. Not Considering Alternatives**

Sometimes alphabet shares or other structures are more appropriate than waivers.

**Alternatives to Dividend Waivers**

**1. Alphabet Shares**

Create different classes of shares (A, B, C, etc.) with the right to pay different dividends to each class. This is often cleaner than waivers.

**2. Different Shareholdings**

If one shareholder should receive more, consider transferring shares (with gift/CGT implications considered).

**3. Salary Payments**

For working shareholders, increased salary might achieve similar outcomes without waiver complications.

**4. Directors'' Loans**

If a shareholder doesn''t need income, leaving dividends as a credit on their director''s loan account (rather than waiving) preserves their entitlement.

**5. No Dividend at All**

If the company doesn''t need to pay dividends, simply don''t declare them.

