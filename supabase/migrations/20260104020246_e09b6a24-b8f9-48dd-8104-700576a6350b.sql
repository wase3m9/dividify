**Understanding Dividend Tax Rates in 2025/26**

The UK operates a tiered system for taxing dividends, with rates depending on which income tax band your total income falls into. For the 2025/26 tax year, the dividend tax rates are:

• **Basic Rate (8.75%)** - Applies to dividends falling within the basic rate band (income up to £50,270)
• **Higher Rate (33.75%)** - Applies to dividends falling within the higher rate band (income between £50,271 and £125,140)
• **Additional Rate (39.35%)** - Applies to dividends for income exceeding £125,140

These rates are significantly lower than the equivalent income tax rates for salary (20%, 40%, and 45%), which is why dividends remain a tax-efficient way to extract profits from your company.

**The Dividend Allowance Explained**

Every UK taxpayer receives a tax-free dividend allowance. For 2025/26, this allowance is **£500**. This means the first £500 of dividends you receive each tax year is completely tax-free, regardless of your income level.

**Important:** The dividend allowance has reduced significantly over recent years:
• 2022/23: £2,000
• 2023/24: £1,000
• 2024/25: £500
• 2025/26: £500

This reduction means directors need to be more strategic about their dividend planning than ever before.

**How Dividend Tax is Calculated**

Dividend tax is calculated after your personal allowance and any salary income. Here is how it works:

**STEP_HEADER Step 1: Calculate Your Total Income**

Add together all your income sources: salary, dividends, rental income, interest, and any other taxable income.

**STEP_HEADER Step 2: Apply Your Personal Allowance**

Deduct your personal allowance (£12,570 for 2025/26) from your total income. Note that the personal allowance reduces by £1 for every £2 earned over £100,000.

**STEP_HEADER Step 3: Apply Your Dividend Allowance**

The first £500 of dividends is tax-free.

**STEP_HEADER Step 4: Calculate Tax on Remaining Dividends**

Apply the appropriate rate based on which tax band your remaining dividends fall into.

