**When do I pay dividend tax?**

Dividend tax is collected through Self Assessment. You will typically pay by 31 January following the end of the tax year, with possible payments on account required in July.

**Can I offset dividend tax with other allowances?**

Your personal allowance is applied to income in a specific order (salary before dividends), but you cannot directly offset dividend tax with other allowances beyond the dividend allowance itself.

**What records do I need to keep?**

You should maintain dividend vouchers for every dividend payment, board minutes authorising dividend declarations, and records of your shareholding structure.

