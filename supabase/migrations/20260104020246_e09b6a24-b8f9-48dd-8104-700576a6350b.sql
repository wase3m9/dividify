Dividend waivers can be a useful tool for managing profit distributions in family companies, but they must be used carefully. HMRC closely scrutinises these arrangements, and getting it wrong can result in significant tax bills. This guide explains when waivers are appropriate and how to implement them correctly.

**What is a Dividend Waiver?**

A dividend waiver is a formal document whereby a shareholder gives up their right to receive a dividend that would otherwise be payable to them. When a dividend is declared, the waiving shareholder receives nothing (or a reduced amount), while other shareholders receive their full entitlement.

**Example:**
A husband and wife each own 50% of a company. The company declares a dividend of £10,000 per share (£20,000 total). The husband waives his dividend entirely. The wife receives the full £10,000 she''s entitled to, while the husband receives nothing.

**Important distinction:** A dividend waiver is different from:
• **Not declaring a dividend** - Where no dividend is paid to anyone
• **Alphabet shares** - Different share classes that can receive different dividends
• **Gift of shares** - Transferring ownership of shares

**When Might You Use a Dividend Waiver?**

**1. Tax Planning Between Spouses**

If one spouse is a higher rate taxpayer and the other is a basic rate taxpayer, a waiver could shift dividend income to the lower-taxed spouse.

**WARNING_BOX This is the most common - and most heavily scrutinised - use of dividend waivers. HMRC frequently challenges these arrangements under the "settlements legislation" (ITTOIA 2005 s624-627).**

**2. Preserving Working Capital**

If a shareholder doesn''t need income and wants to leave their share in the company for reinvestment, they might waive their dividend.

**3. Proportional Distributions**

Sometimes shareholders agree that dividends should be distributed differently from shareholding percentages for genuine commercial reasons.

**4. Managing Cash Flow**

A shareholder might waive a dividend if they know the company cannot afford to pay the full amount to all shareholders.

**Legal Requirements for Valid Waivers**

For a dividend waiver to be legally effective, it must meet specific requirements:

**STEP_HEADER Requirement 1: Made Before the Dividend is Declared**

The waiver must be signed before the dividend declaration. A waiver signed after the dividend is declared is ineffective - you cannot give away income you''ve already become entitled to.

**STEP_HEADER Requirement 2: In Writing**

The waiver should be documented in a formal deed. While a simple letter might suffice, a properly executed deed provides stronger evidence.

**STEP_HEADER Requirement 3: Properly Executed**

As a deed, the waiver should:
• Clearly identify the shareholder waiving
• Specify which dividends are being waived (or state it applies to all dividends until revoked)
• Be signed by the shareholder
• Be witnessed
• Clearly state it is executed as a deed

**STEP_HEADER Requirement 4: Cover a Specific Period**

Waivers can be:
• **Specific** - Covering one particular dividend
• **General** - Covering all dividends until the waiver is revoked

General waivers should include a mechanism for revocation.

**HMRC Scrutiny and Settlements**

HMRC has historically challenged dividend waiver arrangements, particularly between spouses, under the **settlements legislation**.

**The Settlements Legislation (ITTOIA 2005 s624-627)**

This anti-avoidance legislation taxes the "settlor" (the person making the arrangement) on income arising from a "settlement" (an arrangement that shifts income to someone else).

**The Key Case: Arctic Systems (Jones v Garnett)**

In this landmark 2007 case, a husband transferred shares to his wife, who then received dividends. HMRC argued this was a settlement.

**The House of Lords ruled:**
• The initial gift of shares was a settlement
• BUT the "outright gift" exemption applied because the husband had no way to get the shares back
• HMRC lost the case

**However**, this protection may not extend to dividend waivers because:
• The shares aren''t being given away
• The waiver is a new arrangement each time
• The waiver could be revoked

**When HMRC Will Challenge Waivers**

HMRC typically challenges when:
• There is an obvious tax motive
• The waiver benefits a spouse or family member
• There is no clear commercial rationale
• The arrangement is repeated year after year
• The waiving shareholder has contributed work or capital to the company

**Valid Commercial Reasons**

To defend a dividend waiver, you need genuine commercial justification. Examples include:

**1. Retirement Planning**

An older shareholder waives dividends to allow younger shareholders to build capital for the business''s future.

**2. Different Financial Needs**

One shareholder has other income sources and genuinely doesn''t need the dividend, while another relies on it.

**3. Reinvestment Agreement**

Shareholders agree that one party will waive dividends for a period while their share is reinvested in the business.

**4. Cash Flow Management**

The company cannot afford to pay all shareholders, and some voluntarily waive to ensure the business survives.

**5. Non-Contributing Shareholders**

A shareholder who is no longer active in the business waives dividends in favour of those doing the work.

**WARNING_BOX "My spouse is in a lower tax bracket" is NOT a valid commercial reason. The tax saving itself cannot be the primary motivation.**

