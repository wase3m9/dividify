**2. Spread Dividends Across Tax Years**

If you have flexibility, consider timing dividend payments to utilise basic rate bands across multiple tax years rather than pushing into higher rate territory in a single year.

**3. Utilise Spouse or Partner Shareholdings**

If your spouse or civil partner is a shareholder and has unused personal allowance or basic rate band, dividends paid to them will be taxed at their marginal rate. This must reflect genuine shareholding - see our guide on dividend waivers for important compliance considerations.

**4. Consider Pension Contributions**

Making pension contributions through your company reduces Corporation Tax and can be more tax-efficient than taking additional dividends.

**5. Retain Profits in the Company**

If you do not need all the profits personally, leaving them in the company means only paying Corporation Tax (25% for profits over £250,000, or 19-25% for smaller profits) rather than dividend tax on top.

