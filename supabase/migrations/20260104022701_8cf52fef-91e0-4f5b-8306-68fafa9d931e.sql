## Why this matters to UK company directors

Dividends are distributions of post-tax profits. If handled correctly, they are usually more tax-efficient than salary. If handled incorrectly, directors risk an overdrawn loan account, HMRC reclassifying drawings as salary, or unexpected tax bills.

WARNING_BOX: Having the right paperwork and process in place protects directors and makes life easier if HMRC ever asks for evidence.

## Mistake 1: Paying dividends without distributable profits

It is not enough to have cash in the bank. Dividends can only be paid from accumulated post-tax profits. Loans, VAT funds, or supplier payments do not count as profit.

**Things to watch:**

- Check monthly profit and loss reports
- Set aside corporation tax before declaring dividends
- Only declare dividends supported by retained profits

## Mistake 2: Missing paperwork

HMRC can challenge dividends if there are no supporting records. Each dividend should be backed by:

- Board minutes authorising the dividend
- A dividend voucher showing the date, shareholder and amount

WARNING_BOX: Without these, HMRC may argue the payments were salary rather than dividends.

## Mistake 3: Treating bank balance as profit

Some directors withdraw ad hoc amounts based on cash in the bank. This makes it difficult to track drawings and can result in an overdrawn loan account.

**Better practice:**

- Set a regular dividend, monthly or quarterly, that is sustainable
- Use a standing order for discipline
- Pay any extra as a separate dividend with paperwork in place

## Mistake 4: Forgetting the personal tax bill

Dividends are paid gross. The tax is usually due later via Self Assessment. If directors do not plan for this, they may need to declare another dividend just to cover the tax.

TIP_BOX: Put aside a percentage of each dividend for future tax. Review the amount each quarter and ask your accountant to estimate your effective tax rate.

## Mistake 5: Unequal dividends without the right share structure

Dividends must be paid equally per share within the same class. Paying different amounts to shareholders without the right structure can cause problems.

**What to consider:**

- Use alphabet shares (A, B, etc.) if you need flexibility
- Consider a formal dividend waiver if one shareholder should not receive a payment
- Keep share registers and records updated

## Best practice checklist

Before paying dividends, make sure you can tick all of the following:

1. Confirm retained profits before declaring dividends
2. Ring-fence corporation tax estimates
3. Pay dividends regularly rather than ad hoc
4. Keep vouchers and minutes for every dividend
5. Set aside money for personal tax
6. Use the correct share structure for different payouts
7. Keep records neat and accessible

## FAQs

**Can I pay a dividend if the bank balance is healthy but profits are low?**

No. Dividends can only be paid from distributable profits, not just available cash.

**Do I need board minutes for every dividend?**

Yes. Each dividend requires minutes and a voucher. This is quick to produce and keeps you compliant.

**Should dividends be paid monthly or quarterly?**

Either is fine, provided the company has sufficient profits. Monthly dividends are common for predictable cashflow.

**Can spouses receive different dividend amounts?**

Only if you have alphabet shares or a valid waiver. Otherwise, dividends must be paid equally per share.

**What if dividends were over-declared?**

Speak to your accountant as soon as possible. Options may include repaying the drawings or addressing the loan account.

