## What is a Dividend Voucher?

A dividend voucher is a formal document that records the payment of dividends to shareholders. It serves as official proof that a dividend has been paid and must include specific information required by HMRC. Think of it as a receipt for dividend payments – shareholders need it for their tax returns, and your company needs it for accurate record-keeping.

**Key purposes of a dividend voucher:**

- Provides evidence of dividend payments for tax purposes
- Helps shareholders complete their Self Assessment tax returns
- Creates an audit trail for company finances
- Demonstrates compliance with company law

## Legal Requirements for Dividend Vouchers in the UK

Under UK law, specifically the Companies Act 2006 and HMRC regulations, dividend vouchers must contain certain mandatory information. Failing to include this information can lead to complications with tax returns and potential HMRC penalties.

**Every dividend voucher must include:**

- Company name and registration number
- Date the dividend is paid
- Shareholder name and address
- Number of shares held
- Rate of dividend per share
- Total dividend amount
- Tax year for the dividend

Since April 2016, dividends are no longer accompanied by a tax credit, so there''s no need to show a notional tax credit on vouchers issued after this date.

## Step 1: Verify Your Company''s Eligibility to Pay Dividends

Before creating any dividend vouchers, you must ensure your company is legally permitted to pay dividends. This is a critical first step that many directors overlook.

**Eligibility checklist:**

- **Sufficient distributable profits:** Your company must have retained profits available for distribution. Check your most recent accounts to confirm positive retained earnings.
- **All shares fully paid:** Shareholders must have paid for their shares in full before receiving dividends.
- **Company solvency:** Directors must be confident the company can meet its debts for at least 12 months after the dividend payment.
- **Articles of Association compliance:** Review your company''s Articles to check for any restrictions on dividend payments.

**Warning:** Paying dividends when your company doesn''t have sufficient profits is illegal and constitutes an "unlawful distribution." Directors may be personally liable to repay such dividends.

## Step 2: Hold a Board Meeting and Record the Decision

Dividends must be formally declared by the company directors at a board meeting (or by shareholders at a general meeting for final dividends). This ensures proper corporate governance and creates a legal record of the decision.

**How to conduct a dividend declaration meeting:**

- **Schedule the meeting:** Give all directors reasonable notice, even if your company only has one director.
- **Prepare the agenda:** The main item should be "Declaration of dividend" with the proposed amount and payment date.
- **Review the finances:** Present the latest management accounts showing available profits.
- **Make the resolution:** Pass a formal resolution to declare the dividend, specifying the amount per share and payment date.
- **Record the minutes:** Document the meeting in written board minutes, signed and dated by the chairperson.

**Sample resolution wording:**

"It was resolved that an interim dividend of £X.XX per ordinary share be declared, payable on [date] to all shareholders registered on [record date]."

## Step 3: Gather Shareholder Information

Accurate shareholder information is essential for creating correct dividend vouchers. You''ll need complete details for each shareholder receiving a dividend payment.

**Information required for each shareholder:**

- Full legal name (as registered at Companies House)
- Current postal address
- Number of shares held
- Class of shares (if your company has multiple share classes)
- Unique Taxpayer Reference (UTR) for company records

**Where to find this information:**

- Your company''s statutory register of members
- Companies House confirmation statements
- Shareholder agreements
- Previous dividend voucher records

**Tip:** Keep your register of members up to date throughout the year to make dividend processing smoother. Any changes in share ownership should be recorded promptly.

## Step 4: Calculate the Dividend Amount

Once you know your shareholder details, calculate how much each shareholder will receive. The calculation is straightforward but must be done carefully to ensure accuracy.

**Calculation formula:**

Total dividend per shareholder = Number of shares held × Dividend rate per share

**Example calculation:**

If you declare a dividend of £0.50 per share and Shareholder A owns 1,000 shares:

Dividend payment = 1,000 × £0.50 = £500

**Important considerations:**

- **Different share classes:** If your company has multiple classes of shares (e.g., ordinary and preference shares), each class may receive different dividend rates. Calculate separately for each class.
- **Pro-rata payments:** If shares were issued or transferred during the dividend period, you may need to calculate pro-rata amounts.
- **Maximum amounts:** Consider the £500 dividend allowance (2025/26) when planning payments for tax efficiency.
- **Round numbers:** While you can pay dividends in pence, round pounds often simplify accounting.

**Tax planning tip:** Many small company directors pay dividends quarterly or monthly to maximize the annual dividend allowance and manage tax liability efficiently.

