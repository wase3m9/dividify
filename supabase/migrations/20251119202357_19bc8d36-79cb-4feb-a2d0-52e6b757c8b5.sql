## Step 6: Distribute and Store the Voucher

Once created, dividend vouchers must be distributed to shareholders and properly stored for future reference.

**Distribution methods:**

- **Email:** Most common and efficient method. Send as a PDF attachment for security.
- **Post:** For shareholders who prefer physical documents or don''t have email.
- **Hand delivery:** For owner-directors or local shareholders.
- **Secure portal:** Some companies use shareholder portals for document distribution.

**Distribution timing:**

Vouchers should be provided to shareholders:

- At the time of payment (ideal)
- Within a reasonable period after payment (typically within 14 days)
- Before the tax return deadline (essential for shareholders to complete their tax returns)

